    public void actualizarT() {
      
        while (carrito.getRowCount() > 0) {
            carrito.removeRow(0);
        }
        subtotal=0;
        for (Pagar v : carritoVentas) {
            Object o[] = new Object[4];
            o[0] = v.getDescripcion();
            o[1] = v.getPrecio();
            o[2] = v.getCantidad();
            o[3] = v.getImporte();
            
            subtotal+=v.getImporte();           
            carrito.addRow(o);
        }
        descuentos();
        txtsubtotal.setText(moneda(subtotal));
        txtdescuento.setText(moneda(descuento));
        txttotal.setText(moneda(total));
        jTable.setModel(carrito);
    }

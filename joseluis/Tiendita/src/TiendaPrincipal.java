    public boolean nuevaCompra(Pagar n){   
       for (Pagar v : carritoVentas){
            if (v.getId()==n.getId()){
                int nuevaCantidad=v.getCantidad()+n.getCantidad();
                v.setCantidad(nuevaCantidad);
                return true;
            }
        }
        return false;
        
    }

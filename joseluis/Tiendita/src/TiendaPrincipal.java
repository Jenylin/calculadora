    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        int row = jTable.getSelectedRow();
        String dato=String.valueOf(carrito.getValueAt(jTable.getSelectedRow(),3));

        subtotal= subtotal- Double.parseDouble(dato);
        descuentos();
        txtsubtotal.setText(moneda(subtotal));
        txtdescuento.setText(moneda(descuento));
        txttotal.setText(moneda(total));

        carrito.removeRow(row);
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        valorPrecio();

        if (txtcantidad.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Ingresa una Cantidad");
        }else{
            int cantidad = Integer.parseInt(txtcantidad.getText());
            Pagar venta = new Pagar();
            venta.setId(cboproductos.getSelectedIndex());
            venta.setDescripcion((String) cboproductos.getSelectedItem());
            venta.setPrecio(precio);
            venta.setCantidad(cantidad);
            venta.setImporte(precio * cantidad);

            if(!nuevaCompra(venta)){
                carritoVentas.add(venta);
            }
            actualizarT();
            limpiarPant();

        }
    }//GEN-LAST:event_btnagregarActionPerformed

